# Vamos receber dois dados diferentes do usuário e concatena-los em uma única string?!

# Solicitando os dados do usuário
primeiro_dado = input("Por favor, insira o primeiro dado: ")
segundo_dado = input("Por favor, insira o segundo dado: ")

# Concatenando os dados recebidos
dados_concatenados = primeiro_dado + segundo_dado

# Exibindo o resultado da concatenação
print("Dados concatenados:", dados_concatenados)